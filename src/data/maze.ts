export const convertMazeData = (mazeData: number[][]): CellType[][] => {
  return mazeData.map(row => 
    row.map(cell => {
      switch(cell) {
        case 0: return 'wall';
        case 1: return 'pellet';
        case 2: return 'power';
        case 3: return 'path';
        default: return 'path';
      }
    })
  );
};

